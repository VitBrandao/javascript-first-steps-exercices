const num1 = 35;
const num2 = 10;

function sum (x,y) {
    return x + y;
}
console.log('Soma: ' + sum(num1, num2));

function minus (x,y) {
    return x - y;
}
console.log('Subtração: ' + minus(num1, num2));

function multiply(x,y) {
    return x*y;
}
console.log('Multiplicação: ' + multiply(num1, num2));

function division (x,y) {
    return x / y;
}
console.log('Divisão: ' + division(num1, num2));

function resto (x,y) {
    return x % y;
}
console.log('Resto da divisão: ' + resto(num1, num2));